def test_encode_clear_board():
    board = chess.Board()
    board.clear()
    planes = encode(board)
    assert planes == [[[0 for _ in range(8)] for _ in range(8)] for _ in range(12)] 

def test_encode_initial_setup():
    board = chess.Board()
    planes = encode(board)
    assert planes == initial_setup

def test_encode_one_move():
    board = chess.Board()
    board.push_san('e4')
    planes = encode(board)
    assert planes == one_move

def test_encode_two_moves():
    board = chess.Board()
    board.push_san('e4')
    board.push_san('e5')
    planes = encode(board)
    assert planes == two_moves
